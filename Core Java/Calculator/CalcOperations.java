package Calculator;

public class CalcOperations {
    public int add(int n, int m) {
        return n + m;
    }

    public int sub(int n, int m) {
        return n - m;
    }

    public int multi(int n, int m) {
        return n * m;
    }

    public int div(int n, int m) {
        return n / m;
    }
}
